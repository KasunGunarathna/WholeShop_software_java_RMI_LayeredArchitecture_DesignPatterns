    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        CustomerDTO customer = null;
        try {
            customer = ctrlCustomer.get(txtID.getText());
        } catch (Exception ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (customer != null) {
            txtName.setText(customer.getName());
            txtAddress.setText(customer.getAddress());
            txtSalary.setText(String.valueOf(customer.getSalary()));
        } else {
            txtName.setText("");
            txtAddress.setText("");
            txtSalary.setText("");
            JOptionPane.showMessageDialog(this, "There is no such customer in DB");
            txtID.requestFocusInWindow();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void tblCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomerMouseClicked
        DefaultTableModel dtm = (DefaultTableModel) tblCustomer.getModel();
        if (tblCustomer.getSelectedRow() != -1) {
            String select = dtm.getValueAt(tblCustomer.getSelectedRow(),0).toString();
            CustomerDTO customer = null;
        try {
            customer = ctrlCustomer.get(select);
        } catch (Exception ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (customer != null) {
            txtID.setText(customer.getId());
            txtName.setText(customer.getName());
            txtAddress.setText(customer.getAddress());
            txtSalary.setText(String.valueOf(customer.getSalary()));
        } else {
            txtID.setText("");
            txtName.setText("");
            txtAddress.setText("");
            txtSalary.setText("");
            JOptionPane.showMessageDialog(this, "There is no such customer in DB");
            txtID.requestFocusInWindow();
        }
        }
    }//GEN-LAST:event_tblCustomerMouseClicked

