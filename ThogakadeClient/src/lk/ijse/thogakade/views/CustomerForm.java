    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        if (txtID.getText().trim().isEmpty() || txtName.getText().trim().isEmpty()
                || txtAddress.getText().trim().isEmpty() || txtSalary.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter valid data");
            return;
        }

        CustomerDTO customer = new CustomerDTO(
                txtID.getText(),
                txtName.getText(),
                txtAddress.getText(),
                Double.parseDouble(txtSalary.getText())
        );
        boolean result = false;
        try {
            boolean reserve = ctrlCustomer.reserve(txtID.getText());
            if (reserve) {
                result = ctrlCustomer.add(customer);
                if (result) {
                    ctrlCustomer.release(txtID.getText());
                    JOptionPane.showMessageDialog(this, "Customer has been successfully added");
                    refreshTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Customer adding Faild..!!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please Wait...!");
            }
        } catch (Exception ex) {
            SQLException exp = (SQLException) ex;
            System.out.println(exp.getErrorCode());
            if(exp.getErrorCode()==1062){
                JOptionPane.showMessageDialog(this, "Customer has been already added");
            }
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnAddActionPerformed
    
