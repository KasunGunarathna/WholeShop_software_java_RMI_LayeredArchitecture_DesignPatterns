    private void tblItemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblItemMouseClicked
        DefaultTableModel dtm = (DefaultTableModel) tblItem.getModel();
        if (tblItem.getSelectedRow() != -1) {
            String select = dtm.getValueAt(tblItem.getSelectedRow(),0).toString();
            ItemDTO customer = null;
        try {
            customer = ctrlItem.get(select);
        } catch (Exception ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (customer != null) {
            txtCode.setText(customer.getCode());
            txtDescription.setText(customer.getCode());
            txtUnitPrice.setText(String.valueOf(customer.getUnitPrice()));
            txtQtyOnHand.setText(String.valueOf(customer.getQtyOnHand()));
        } else {
            txtCode.setText("");
            txtDescription.setText("");
            txtUnitPrice.setText("");
            txtQtyOnHand.setText("");
            JOptionPane.showMessageDialog(this, "There is no such customer in DB");
            txtCode.requestFocusInWindow();
        }
        }
    }//GEN-LAST:event_tblItemMouseClicked

