    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        if (txtCode.getText().trim().isEmpty() || txtDescription.getText().trim().isEmpty()
                || txtUnitPrice.getText().trim().isEmpty() || txtQtyOnHand.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter valid data");
            return;
        }
        
        ItemDTO item = new ItemDTO(
                txtCode.getText(),
                txtDescription.getText(),
                Double.parseDouble(txtUnitPrice.getText()),
                Integer.parseInt(txtQtyOnHand.getText())
        );
        boolean result = false;
        try {
              boolean reserve = ctrlItem.reserve(txtCode.getText());
              if (reserve) {
                result = ctrlItem.add(item);
                if (result) {
                    ctrlItem.release(txtCode.getText());
                    JOptionPane.showMessageDialog(this, "Item has been successfully added");
                    refreshTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Item adding Faild..!!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please Wait...!");
            }
        } catch (Exception ex) {
            SQLException exp = (SQLException) ex;
            System.out.println(exp.getErrorCode());
            if(exp.getErrorCode()==1062){
                JOptionPane.showMessageDialog(this, "Item has been already added");
            }
            Logger.getLogger(ItemForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

