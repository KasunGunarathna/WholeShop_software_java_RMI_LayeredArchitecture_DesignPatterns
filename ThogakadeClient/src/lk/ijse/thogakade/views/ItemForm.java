    public void refreshTable(){
        ArrayList<ItemDTO> allItems = null;
        
        try {
            allItems = ctrlItem.getAll();
        } catch (Exception ex) {
            Logger.getLogger(ItemForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        

        DefaultTableModel dtm = (DefaultTableModel) tblItem.getModel();
        dtm.setRowCount(0);

        for (ItemDTO item : allItems) {
            Object[] rowData = {item.getCode(),
                item.getDescription(),
                item.getUnitPrice(),
                item.getQtyOnHand()};
            dtm.addRow(rowData);
        }
    }
